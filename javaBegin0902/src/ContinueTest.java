
public class ContinueTest {

	public static void main(String[] args) {
//		Q. 0부터 10까지의 정수 중에서  
//		   3~6을 제외한 나머지 출력
		for(int i=0 ; i<=10 ; i++){
			if( i>2 && i<7){
				continue;
			}
			System.out.print(i + " ");
		}
		
//		Q. 1부터 10사이의 정수 중  
//		   3, 6, 9만 제외하고 출력
//
//		Q. 1부터 100 사이의 7의 배수의 값 출력

	}

}
